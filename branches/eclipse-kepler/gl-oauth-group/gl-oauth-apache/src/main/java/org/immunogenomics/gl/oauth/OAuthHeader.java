package org.immunogenomics.gl.oauth;

/**
 * HTTP header names used with OAuth 2.0 <a
 * href="http://tools.ietf.org/html/rfc6750">OAuth 2.0 Bearer Token</a>.
 */
public final class OAuthHeader {

    /** OAuth 2.0 authorization request header. */
    public static final String AUTHORIZATION = "Authorization";
    /** OAuth 2.0 authenticate response header. */
    public static final String WWW_AUTHENTICATE = "WWW-Authenticate";

}
